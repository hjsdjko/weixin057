			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect
		},
		watch:{
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.jiazhangzhanghao = this.user.jiazhangzhanghao
			this.ro.jiazhangzhanghao = true;
			this.ruleForm.jiazhangxingming = this.user.jiazhangxingming
			this.ro.jiazhangxingming = true;


			// 下拉框
			res = await this.$api.option(`banjixinxi`,`banji`,{});
			this.banjiOptions = res.data;
            this.banjiOptions.unshift("请选择班级");
			// 下2
			res = await this.$api.option(`jiaoshi`,`jiaoshigonghao`,{});
			this.jiaoshigonghaoOptions = res.data;
			// 自定义下拉框值
			this.pingjiadengjiOptions = "优秀,良好,一般".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jiaoshipingjia`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='banji'){
					this.ruleForm.banji = obj[o];
					this.ro.banji = true;
					continue;
					}
					if(o=='jiaoshigonghao'){
					this.ruleForm.jiaoshigonghao = obj[o];
					this.ro.jiaoshigonghao = true;
					continue;
					}
					if(o=='jiaoshixingming'){
					this.ruleForm.jiaoshixingming = obj[o];
					this.ro.jiaoshixingming = true;
					continue;
					}
					if(o=='pingjiadengji'){
					this.ruleForm.pingjiadengji = obj[o];
					this.ro.pingjiadengji = true;
					continue;
